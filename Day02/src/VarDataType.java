// 기본 자료형
// 숫자형 (+char)
// [int], long, short, byte, char(문자형/숫자형)
// 실수형 소수점
// float, [double]
// 논리형
// boolean (bool)

public class VarDataType {
	public static void main(String[] args) {
		System.out.println(10);
		char c = 64;
		System.out.println(c);
		//System.out.println((char)c);
		
		float fo = 0.11f; // float형은 f를 붙여야 에러가 나지 않는다.(실수형 기본형은 double)
		double db = 0.12;
		
		boolean isPerson = false; // true
		
		//-- Class면서 문자열
		String name = "박경란";
		char n = '박';
		
		System.out.println(name);
		
		// 기본자료형		
		// int      = Integer
		// boolean  = Boolean
		// double   = Double
		
		//lodossw@gmail.com ->선생님 메일
		
	}

}
